    private void boton_grabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_grabarActionPerformed
        
        //boton para grabar
        
        String posicion = indice.getText(); //tomo el valor que se muestra en el text field del indice
        pos = Integer.parseInt(posicion); //como necesito usarlo como int para moverme por los array lo convierto
        
        //guardo los datos ingresados en los text field en los array con las posicion que tome del text field indice
        dni [pos] = texto_dni.getText();
        nombre [pos] = texto_nombre.getText();
        apellido [pos] = texto_apellido.getText();
        direccion [pos] = texto_direccion.getText();
        telefono [pos] = texto_telefono.getText();
        Fnac [pos] = texto_fecha.getText();
        
           
    }//GEN-LAST:event_boton_grabarActionPerformed

