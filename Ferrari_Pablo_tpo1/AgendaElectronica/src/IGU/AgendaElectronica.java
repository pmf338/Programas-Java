    private void boton_avanzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_avanzarActionPerformed
        
        //boton para avanzar
        String posicion = indice.getText(); //tomo el valor que se muestra en el text field del indice
        pos = Integer.parseInt(posicion); //lo convierto a int
        pos ++; // incremento el indice en 1
        
        if(pos<dni.length) // compruebo que la posicion del text field indice no sobrepase las posiciones del array
        {
            
            
        //cuando cambio de posicion muestro lo que este guardado en la nueva posicion
        texto_dni.setText(dni[pos]);
        texto_nombre.setText(nombre[pos]);
        texto_apellido.setText(apellido[pos]);
        texto_direccion.setText(direccion[pos]);
        texto_telefono.setText(telefono[pos]);
        texto_fecha.setText(Fnac[pos]);
        
        indice.setText(Integer.toString(pos));//vuelvo a convertir el indice en String para regresarlo al text field indice
        
        }
        
        
        
        
    }//GEN-LAST:event_boton_avanzarActionPerformed

    private void boton_retroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boton_retroActionPerformed
        
        String posicion = indice.getText(); //tomo el valor que se muestra en el text field del indice
        pos = Integer.parseInt(posicion); //lo convierto a int
        pos --; //decremento el indice
        
        if(pos>-1) // compruebo que la posicion del text field indice sea valida con las posiciones del array
        {
            
            
        //cuando cambio de posicion muestro lo que este guardado en la nueva posicion
        texto_dni.setText(dni[pos]);
        texto_nombre.setText(nombre[pos]);
        texto_apellido.setText(apellido[pos]);
        texto_direccion.setText(direccion[pos]);
        texto_telefono.setText(telefono[pos]);
        texto_fecha.setText(Fnac[pos]);
        
        indice.setText(Integer.toString(pos)); //vuelvo a convertir el indice en String para pasarlo al text field indice
        
        }
    }//GEN-LAST:event_boton_retroActionPerformed

   
   

