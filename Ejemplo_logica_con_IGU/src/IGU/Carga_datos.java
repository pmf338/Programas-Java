    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        
        int cod_prod = Integer.parseInt(txtCod.getText());
        String nombre = txtNombre.getText();
        int cant = Integer.parseInt(txtCant.getText());
        String marca = txtMarca.getText();
        String tipo = (String) cmbTipo.getSelectedItem(); //cuando traigo algo de un combo viene como tipo Object que es una especie
        Date fecha_venc = jFechaVenc.getDate();           //de objeto mayor a los demas y se puede convertir a cualquier tipo casteandolo
        
        control.altaProducto(cod_prod, nombre, cant, marca, tipo, fecha_venc); //llamo al metodo que se encuentra en la clase controladora
        
    }//GEN-LAST:event_btnAgregarActionPerformed

